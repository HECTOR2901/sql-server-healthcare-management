-- 3. Análisis de Concentración de Inventario por Categoría
SELECT 
    C.Nombre_Categoria,
    CAST(SUM(D.Total_USD) AS DECIMAL(10,2)) AS Inversion_Categoria,
    CAST((SUM(D.Total_USD) * 100.0 / SUM(SUM(D.Total_USD)) OVER()) AS DECIMAL(5,2)) AS Porcentaje_Inversion,
    CASE 
        WHEN (SUM(D.Total_USD) * 100.0 / SUM(SUM(D.Total_USD)) OVER()) > 40 THEN 'CATEGORÍA ESTRATÉGICA (A)'
        WHEN (SUM(D.Total_USD) * 100.0 / SUM(SUM(D.Total_USD)) OVER()) BETWEEN 15 AND 40 THEN 'CATEGORÍA OPERATIVA (B)'
        ELSE 'CATEGORÍA MENOR (C)'
    END AS Clasificacion_ABC
FROM Sourcing.Orden_Compra_D D
INNER JOIN Inventario.Insumos_Medicos I ON D.ID_Material = I.ID_Material
INNER JOIN Inventario.Categorias C ON I.ID_Categoria = C.ID_Categoria
GROUP BY C.Nombre_Categoria
ORDER BY Inversion_Categoria DESC;

-- BLOQUE 4: GESTIÓN DE ESPECIALIDADES Y DOCTORAS

-- 1. Análisis de consumo y gasto de insumos por especialidad médica
-- Objetivo: Analizar el consumo y gasto de insumos por especialidad para identificar las áreas con mayor demanda de recursos.
SELECT
    e.Nombre_Especialidad,
    ISNULL(SUM(od.Cantidad),0) AS Total_Insumos,
    ISNULL(SUM(od.Cantidad * od.Precio_Unitario),0) AS Gasto_Total
FROM Personal.Especialidades e
LEFT JOIN Personal.Doctores d
    ON d.ID_Especialidad = e.ID_Especialidad
LEFT JOIN Sourcing.Orden_Compra_H oc
    ON d.ID_Doctor = oc.ID_Doctor
LEFT JOIN Sourcing.Orden_Compra_D od
    ON oc.Nro_OC = od.Nro_OC
GROUP BY e.Nombre_Especialidad
HAVING SUM(od.Cantidad) > 0
ORDER BY Gasto_Total DESC;

-- 2. Análisis de concentración del gasto de insumos por doctor
-- Objetivo: Identificar qué doctores generan mayor gasto en insumos médicos dentro de sus especialidades.
SELECT
    d.Nombre_Doctor,
    e.Nombre_Especialidad,
    ISNULL(SUM(od.Cantidad),0) AS Total_Insumos_Utilizados,
    ISNULL(SUM(od.Cantidad * od.Precio_Unitario),0) AS Gasto_Total
FROM Personal.Doctores d
LEFT JOIN Personal.Especialidades e
    ON d.ID_Especialidad = e.ID_Especialidad
LEFT JOIN Sourcing.Orden_Compra_H oc
    ON d.ID_Doctor = oc.ID_Doctor
LEFT JOIN Sourcing.Orden_Compra_D od
    ON oc.Nro_OC = od.Nro_OC
GROUP BY 
    d.Nombre_Doctor,
    e.Nombre_Especialidad
ORDER BY Gasto_Total DESC;

-- 3. Análisis de distribución de doctores por especialidad médica
-- Objetivo: Identificar cantidad de doctores asignados a cada especialidad
SELECT
    e.Nombre_Especialidad,
    COUNT(d.ID_Doctor) AS Total_Doctor
FROM Personal.Especialidades e
LEFT JOIN Personal.Doctores d
    ON e.ID_Especialidad = d.ID_Especialidad
GROUP BY e.Nombre_Especialidad
ORDER BY Total_Doctor DESC;

-- BLOQUE 5: EFICIENCIA OPERATIVA

-- 1. Tasa de Órdenes Aprobadas vs Pendientes por Mes
SELECT 
    FORMAT(Fecha_Creacion, 'yyyy') AS Ańo,
    COUNT(*) AS Total_OC,
    SUM(CASE WHEN Estado = 'Pagado'    THEN 1 ELSE 0 END) AS Pagado,
    SUM(CASE WHEN Estado = 'Pendiente' THEN 1 ELSE 0 END) AS Pendiente,
    SUM(CASE WHEN Estado = 'Cancelado' THEN 1 ELSE 0 END) AS Cancelado,
    CAST(
        SUM(CASE WHEN Estado = 'Pagado' THEN 1 ELSE 0 END) * 100.0 / COUNT(*) 
    AS DECIMAL(5,2)) AS Tasa_Pagado_Pct,
    CAST(
        SUM(CASE WHEN Estado = 'Cancelado' THEN 1 ELSE 0 END) * 100.0 / COUNT(*) 
    AS DECIMAL(5,2)) AS Tasa_Cancelado_Pct,
	CAST(
		SUM(CASE WHEN Estado = 'Pendiente' THEN 1 ELSE 0 END) * 100.0 / COUNT(*) 
	AS DECIMAL(5,2)) AS Tasa_Pendiente_Pct
FROM Sourcing.Orden_Compra_H
GROUP BY FORMAT(Fecha_Creacion, 'yyyy')
ORDER BY Ańo

-- 2. Concentración de Gasto por Proveedor (Índice de Dependencia)
SELECT 
    P.Nombre_Proveedor,
    COUNT(DISTINCT H.Nro_OC) AS Cantidad_OC,
    SUM(D.Total_USD) AS Gasto_Total,
    CAST(
        SUM(D.Total_USD) * 100.0 / SUM(SUM(D.Total_USD)) OVER () 
    AS DECIMAL(5,2)) AS Participacion_Pct,
    CASE 
        WHEN SUM(D.Total_USD) * 100.0 / SUM(SUM(D.Total_USD)) OVER () > 30 
        THEN 'RIESGO ALTO'
        WHEN SUM(D.Total_USD) * 100.0 / SUM(SUM(D.Total_USD)) OVER () > 15 
        THEN 'RIESGO MEDIO'
        ELSE 'NORMAL'
    END AS Nivel_Dependencia
FROM Sourcing.Proveedores P
JOIN Sourcing.Orden_Compra_H H ON P.ID_Proveedor = H.ID_Proveedor
JOIN Sourcing.Orden_Compra_D D ON H.Nro_OC = D.Nro_OC
GROUP BY P.Nombre_Proveedor
ORDER BY Gasto_Total DESC

-- 3. Eficiencia Operativa por Tipo de Atención
SELECT 
    PS.Descripcion_Prioridad AS Tipo_Atencion,
    COUNT(DISTINCT D.Nro_OC) AS OC_Involucradas,
    SUM(D.Cantidad) AS Unidades_Solicitadas,
