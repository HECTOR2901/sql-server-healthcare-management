-- 2. Creación de Esquemas (Separación de Responsabilidades)
CREATE SCHEMA Personal;
GO
CREATE SCHEMA Inventario; 
GO
CREATE SCHEMA Sourcing; 
GO

--- =============================================
--- I. ESQUEMA PERSONAL
--- =============================================

CREATE TABLE Personal.Especialidades (
    ID_Especialidad CHAR(4) PRIMARY KEY,
    Nombre_Especialidad VARCHAR(50),
    Piso_Ubicacion VARCHAR(50)
);

CREATE TABLE Personal.Doctores (
    ID_Doctor CHAR(6) PRIMARY KEY,
    Nombre_Doctor VARCHAR(100),
    CMP_Numero CHAR(6),
    ID_Especialidad CHAR(4) FOREIGN KEY REFERENCES Personal.Especialidades(ID_Especialidad)
);

CREATE TABLE Personal.Prioridad_Servicio (
    ID_Prioridad CHAR(5) PRIMARY KEY,
    Descripcion_Prioridad VARCHAR(50)
);

--- =============================================
--- II. ESQUEMA INVENTARIO
--- =============================================

CREATE TABLE Inventario.Categorias (
    ID_Categoria INT PRIMARY KEY,
    Nombre_Categoria VARCHAR(50)
);

CREATE TABLE Inventario.Unidad_Medida (
    ID_UM VARCHAR(5) PRIMARY KEY,
    Nombre_UM VARCHAR(50)
);

CREATE TABLE Inventario.Insumos_Medicos (
    ID_Material CHAR(8) PRIMARY KEY,
    Nombre_Material VARCHAR(150),
    ID_Categoria INT FOREIGN KEY REFERENCES Inventario.Categorias(ID_Categoria),
    Stock_Minimo INT
);

--- =============================================
--- III. ESQUEMA SOURCING
--- =============================================

CREATE TABLE Sourcing.Proveedores (
    ID_Proveedor CHAR(6) PRIMARY KEY,
    Nombre_Proveedor VARCHAR(100),
    RUC CHAR(11)
);

CREATE TABLE Sourcing.Metodo_Pago (
    ID_Pago CHAR(6) PRIMARY KEY,
    Descripcion_Pago VARCHAR(50)
);

-- Tabla de Hechos: Cabecera
CREATE TABLE Sourcing.Orden_Compra_H (
    Nro_OC CHAR(10) PRIMARY KEY,
    Fecha_Creacion DATE,
    ID_Proveedor CHAR(6) FOREIGN KEY REFERENCES Sourcing.Proveedores(ID_Proveedor),
    ID_Doctor CHAR(6) FOREIGN KEY REFERENCES Personal.Doctores(ID_Doctor),
    ID_Pago CHAR(6) FOREIGN KEY REFERENCES Sourcing.Metodo_Pago(ID_Pago),
    Estado VARCHAR(20)
);

-- Tabla de Hechos: Detalle
CREATE TABLE Sourcing.Orden_Compra_D (
    ID_Detalle INT IDENTITY(1,1) PRIMARY KEY,
    Nro_OC CHAR(10) FOREIGN KEY REFERENCES Sourcing.Orden_Compra_H(Nro_OC),
    ID_Material CHAR(8) FOREIGN KEY REFERENCES Inventario.Insumos_Medicos(ID_Material),
    ID_Prioridad CHAR(5) FOREIGN KEY REFERENCES Personal.Prioridad_Servicio(ID_Prioridad),
    ID_UM VARCHAR(5) FOREIGN KEY REFERENCES Inventario.Unidad_Medida(ID_UM),
    Cantidad INT,
    Precio_Unitario DECIMAL(12,2),
    Fecha_Vencimiento DATE,
    Total_USD AS (Cantidad * Precio_Unitario) -- Columna calculada automática
);
GO


