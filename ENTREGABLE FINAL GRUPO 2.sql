    CAST(
        SUM(D.Total_USD) * 100.0 / SUM(SUM(D.Total_USD)) OVER () 
    AS DECIMAL(5,2)) AS Pct_Del_Gasto_Total
FROM Sourcing.Orden_Compra_D D
JOIN Personal.Prioridad_Servicio PS ON D.ID_Prioridad = PS.ID_Prioridad
GROUP BY PS.Descripcion_Prioridad
ORDER BY Gasto_Total_USD DESC;