-- 4. ANÁLISIS DE RESULTADOS

-- BLOQUE 1: ESTANDARIZACIÓN Y ANÁLISIS TEMPORAL

-- 1. Análisis de Gasto Mensual
-- Objetivo: Identificar picos de demanda presupuestaria durante el ańo.
SELECT 
    DATENAME(MONTH, H.Fecha_Creacion) AS Mes, 
    SUM(D.Total_USD) AS Gasto_Total
FROM Sourcing.Orden_Compra_H H
INNER JOIN Sourcing.Orden_Compra_D D ON H.Nro_OC = D.Nro_OC
GROUP BY DATENAME(MONTH, H.Fecha_Creacion), MONTH(H.Fecha_Creacion)
ORDER BY MONTH(H.Fecha_Creacion);

-- 2. Análisis por Prioridad Hospitalaria
-- Objetivo: Comparar la inversión en pedidos
SELECT 
    ID_Prioridad, 
    COUNT(*) AS Numero_Lineas, 
    SUM(Total_USD) AS Inversion_Total
FROM Sourcing.Orden_Compra_D
GROUP BY ID_Prioridad
ORDER BY Inversion_Total DESC;

-- 3. Segmentación por Rango de Valor
-- Objetivo: Categorizar las compras según su impacto financiero.
SELECT 
    Categoria_Inversion,
    COUNT(*) AS Cantidad_Items
FROM (
    SELECT 
        CASE
            WHEN Total_USD < 1000 THEN 'Gasto Operativo Bajo'
            WHEN Total_USD BETWEEN 1000 AND 5000 THEN 'Gasto Medio'
            ELSE 'Inversión Crítica'
        END AS Categoria_Inversion
    FROM Sourcing.Orden_Compra_D
) t
GROUP BY Categoria_Inversion;

-- BLOQUE 2: GESTIÓN DE PROVEEDORES - SOURCING

-- 1. Analisis de participación de gasto y frecuencia de compra por proveedor
-- Objetivo: Evaluar la eficiencia operativa y comercial de los proveedores
SELECT 
    P.Nombre_Proveedor,
    COUNT(DISTINCT H.Nro_OC) AS Cantidad_Ordenes_Compra,
    SUM(D.Cantidad) AS Cantidad_Total_Suministrada,
    AVG(D.Precio_Unitario) AS Precio_Unitario_Promedio,
    SUM(D.Cantidad * D.Precio_Unitario) AS Inversion_Total_Acumulada
FROM Sourcing.Proveedores P
INNER JOIN Sourcing.Orden_Compra_H H ON P.ID_Proveedor = H.ID_Proveedor
INNER JOIN Sourcing.Orden_Compra_D D ON H.Nro_OC = D.Nro_OC
GROUP BY 
    P.Nombre_Proveedor
ORDER BY Inversion_Total_Acumulada DESC;


-- 2. Análisis de Capacidad de Compra y Ticket Promedio por Orden
-- Objetivo: Optimizar la logística y el esfuerzo administrativo
SELECT 
    P.Nombre_Proveedor,
    COUNT(DISTINCT H.Nro_OC) AS Total_Ordenes,
    SUM(D.Cantidad * D.Precio_Unitario) AS Inversion_Total,
    ROUND(SUM(D.Cantidad * D.Precio_Unitario) / COUNT(DISTINCT H.Nro_OC), 2) AS Ticket_Promedio_OC
FROM Sourcing.Proveedores P
INNER JOIN Sourcing.Orden_Compra_H H ON P.ID_Proveedor = H.ID_Proveedor
INNER JOIN Sourcing.Orden_Compra_D D ON H.Nro_OC = D.Nro_OC
GROUP BY P.Nombre_Proveedor
ORDER BY Ticket_Promedio_OC DESC;


-- 3. Análisis de la estructura de obligaciones financieras por método de pago
-- Objetivo: Optimizar la estrategia de pagos
SELECT 
    MP.Descripcion_Pago,
    COUNT(H.Nro_OC) AS Total_Transacciones,
    SUM(D.Total_USD) AS Inversion_Total_USD,
    CAST(
        SUM(D.Total_USD) * 100.0 / SUM(SUM(D.Total_USD)) OVER() 
    AS DECIMAL(5,2)) AS Pct_Impacto_Caja,
    CASE 
        WHEN MP.Descripcion_Pago LIKE '%Crédito%' THEN 'Favorece Liquidez'
        ELSE 'Exige Desembolso Inmediato'
    END AS Efecto_en_Flujo_Caja
FROM Sourcing.Metodo_Pago MP
INNER JOIN Sourcing.Orden_Compra_H H ON MP.ID_Pago = H.ID_Pago
INNER JOIN Sourcing.Orden_Compra_D D ON H.Nro_OC = D.Nro_OC
GROUP BY MP.Descripcion_Pago
ORDER BY Inversion_Total_USD DESC;

-- BLOQUE 3: CONTROL DE INVENTARIO Y CALIDAD

-- 1. Análisis de gestión de Caducidad y Disponibilidad
-- Clasificación de frescura de insumos usando CASE y Operaciones de Fecha
SELECT 
    ID_Material,
    ID_Prioridad,
    Fecha_Vencimiento,
    DATEDIFF(DAY, GETDATE(), Fecha_Vencimiento) AS Dias_Restantes,
    -- Lógica de Estado de Calidad
    CASE 
        WHEN DATEDIFF(MONTH, GETDATE(), Fecha_Vencimiento) <= 12 THEN 'ROTACIÓN INMEDIATA'
        WHEN DATEDIFF(MONTH, GETDATE(), Fecha_Vencimiento) BETWEEN 13 AND 24 THEN 'STOCK SEGURO'
        ELSE 'ROTACIÓN LARGO PLAZO'
    END AS Categoria_Calidad,
    -- Lógica de Disponibilidad (Uso de negativos simbólicos para alertas)
    CASE 
        WHEN DATEDIFF(DAY, GETDATE(), Fecha_Vencimiento) < 0 THEN 'SISTEMA: LOTE VENCIDO (RETIRAR)'
        WHEN Cantidad = 0 THEN 'ALERTA: SIN STOCK'
        ELSE 'DISPONIBLE'
    END AS Estado_Disponibilidad
FROM Sourcing.Orden_Compra_D
WHERE ID_Prioridad IN ('URGEN', 'QUIRU', 'HOSPI')
-- Orden
ORDER BY 
    CASE 
        WHEN DATEDIFF(MONTH, GETDATE(), Fecha_Vencimiento) <= 12 THEN 1
        WHEN DATEDIFF(MONTH, GETDATE(), Fecha_Vencimiento) BETWEEN 13 AND 24 THEN 2
        ELSE 3
    END, 
    Fecha_Vencimiento ASC;

